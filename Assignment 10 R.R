Package: Sainath
Title: Analyze and Visualize DNA Sequences
Version: 0.1
Authors@R:("RamSainathReddy", "Guddeti", "guddetir@usf.edu, role = c("aut", "cre"))
Description: The Sainath package provides tools to analyze DNA sequences,
including functions to calculate the frequency of each nucleotide (A, T, C, G)
within a sequence, sequence length,and nucleotide frequencies. It also includes 
visualization functions to help users explore and understand DNA sequence properties.
